document.getElementById("infoBtn1").addEventListener("click", function() {
    var infoContent1 = document.getElementById("infoContent1");
    if (infoContent1.style.display == "none") {
        infoContent1.style.display = "block";
    } else {
        infoContent1.style.display = "none";
    }
});

